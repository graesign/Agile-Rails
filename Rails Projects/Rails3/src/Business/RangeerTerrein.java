package Business;

public class RangeerTerrein {
	private int aantalVrijeShuttles;

	private int totaalAantalShuttles;

	private int aantalMogelijkePlaatsen;

	public RangeerTerrein() {
	}

	public int getAantalVrijeShuttles() {
		return aantalVrijeShuttles;
	}

	public void setAantalVrijeShuttles(int val) {
		this.aantalVrijeShuttles = val;
	}

	public int getTotaalAantalShuttles() {
		return totaalAantalShuttles;
	}

	public void setTotaalAantalShuttles(int val) {
		this.totaalAantalShuttles = val;
	}

	public int getAantalMogelijkePlaatsen() {
		return aantalMogelijkePlaatsen;
	}

	public void setAantalMogelijkePlaatsen(int val) {
		this.aantalMogelijkePlaatsen = val;
	}
}
